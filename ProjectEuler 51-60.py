def countLychrelNumbers(upper_limit):
    """assumes upper_limit is an int, the number up to which to look for Lychrel numbers
    returns an int, the number of Luchrel numbers under upeer_limit
    """
    # pdb.set_trace()
    lychrel_count = 0
